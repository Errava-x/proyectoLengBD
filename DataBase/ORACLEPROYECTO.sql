/*Creación de tablespace. Separar y Organizar físicamente los datos del proyecto HappyPaws Veterinaria*/ 

CREATE TABLESPACE TS_HAPPYPAWS
DATAFILE 'C:\ORACLEDB\ORADATA\ORCL\TS_HAPPYPAWS01.DBF'
SIZE 200M
AUTOEXTEND ON NEXT 50M MAXSIZE 2G
EXTENT MANAGEMENT LOCAL
SEGMENT SPACE MANAGEMENT AUTO;



/*EVIDENCIA: confirmar que el tablespace existe*/
SELECT TABLESPACE_NAME, STATUS, CONTENTS
FROM DBA_TABLESPACES
WHERE TABLESPACE_NAME = 'TS_HAPPYPAWS';

/*EVIDENCIA: confirmar el datafile y tamaño del tablespace*/
SELECT TABLESPACE_NAME, FILE_NAME, BYTES/1024/1024 AS MB, AUTOEXTENSIBLE
FROM DBA_DATA_FILES
WHERE TABLESPACE_NAME = 'TS_HAPPYPAWS';

