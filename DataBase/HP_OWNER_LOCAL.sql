/* Creación Tablas MASCOTAS */
/* Registrar mascotas y relacionarlas con clientes */

CREATE TABLE MASCOTAS (
  ID_MASCOTA        NUMBER(10)   PRIMARY KEY,
  NOMBRE            VARCHAR2(50)  NOT NULL,
  ESPECIE           VARCHAR2(50)  NOT NULL,
  RAZA              VARCHAR2(50),
  EDAD              NUMBER(3),
  FECHA_NACIMIENTO  DATE,
  ESTADO            VARCHAR2(30),
  SEXO              VARCHAR2(10),
  COLOR             VARCHAR2(30),
  PESO              NUMBER(5,2)
);

CREATE TABLE CLIENTES_MASCOTAS (
  ID_CLIENTE_MASCOTA NUMBER(10) PRIMARY KEY,
  ID_CLIENTE         NUMBER(12) NOT NULL,
  ID_MASCOTA         NUMBER(10) NOT NULL,
  CONSTRAINT FK_CM_CLIENTE
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE),
  CONSTRAINT FK_CM_MASCOTA
    FOREIGN KEY (ID_MASCOTA) REFERENCES MASCOTAS(ID_MASCOTA)
);

/* Comando para evitar duplicados de la misma relación cliente-mascota */
ALTER TABLE CLIENTES_MASCOTAS
ADD CONSTRAINT UK_CM_CLIENTE_MASCOTA UNIQUE (ID_CLIENTE, ID_MASCOTA);

/* EVIDENCIA: ver tablas creadas */
SELECT TABLE_NAME
FROM USER_TABLES
WHERE TABLE_NAME IN ('MASCOTAS','CLIENTES_MASCOTAS')
ORDER BY TABLE_NAME;

