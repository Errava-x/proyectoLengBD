/*Creación tablas CITAS */
/*Agendar citas por mascota y relacionarlas con médicos*/

CREATE TABLE CITAS (
  ID_CITA        NUMBER(10)    PRIMARY KEY,
  FECHA          DATE          NOT NULL,
  HORA           VARCHAR2(8)   NOT NULL,   
  TIPO_CONSULTA  VARCHAR2(100) NOT NULL,
  ESTADO         VARCHAR2(30)  NOT NULL,
  OBSERVACIONES  VARCHAR2(250),
  ID_MASCOTA     NUMBER(10)    NOT NULL,
  CONSTRAINT FK_CITA_MASCOTA
    FOREIGN KEY (ID_MASCOTA) REFERENCES MASCOTAS(ID_MASCOTA)
);

CREATE TABLE MEDICOS_CITAS (
  ID_MEDICO_CITA NUMBER(10) PRIMARY KEY,
  ID_MEDICO      NUMBER(12) NOT NULL,
  ID_CITA        NUMBER(10) NOT NULL,
  CONSTRAINT FK_MC_MEDICO
    FOREIGN KEY (ID_MEDICO) REFERENCES MEDICOS(ID_MEDICO),
  CONSTRAINT FK_MC_CITA
    FOREIGN KEY (ID_CITA) REFERENCES CITAS(ID_CITA)
);

/*Comando para evitar duplicados del mismo médico en la misma cita */
ALTER TABLE MEDICOS_CITAS
ADD CONSTRAINT UK_MC_MEDICO_CITA UNIQUE (ID_MEDICO, ID_CITA);

/* EVIDENCIA: ver tablas creadas */
SELECT TABLE_NAME
FROM USER_TABLES
WHERE TABLE_NAME IN ('CITAS','MEDICOS_CITAS')
ORDER BY TABLE_NAME;

