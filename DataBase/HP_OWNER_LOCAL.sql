/*Creación de tablas VENTAS */
/* Registrar ventas y asociarlas a clientes */

CREATE TABLE VENTAS (
  ID_VENTA        NUMBER(10)    PRIMARY KEY,
  PRODUCTO        VARCHAR2(100),
  SERVICIO        VARCHAR2(100),
  PRECIO_UNITARIO NUMBER(10,2)  NOT NULL,
  MONTO_TOTAL     NUMBER(10,2)  NOT NULL,
  METODO_PAGO     VARCHAR2(30)  NOT NULL,
  FECHA           DATE          NOT NULL,
  CONSTRAINT CK_VENTAS_PRECIO CHECK (PRECIO_UNITARIO >= 0),
  CONSTRAINT CK_VENTAS_MONTO  CHECK (MONTO_TOTAL >= 0)
);

CREATE TABLE CLIENTES_VENTAS (
  ID_CLIENTE_VENTA NUMBER(10) PRIMARY KEY,
  ID_CLIENTE       NUMBER(12) NOT NULL,
  ID_VENTA         NUMBER(10) NOT NULL,
  CONSTRAINT FK_CV_CLIENTE
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE),
  CONSTRAINT FK_CV_VENTA
    FOREIGN KEY (ID_VENTA) REFERENCES VENTAS(ID_VENTA)
);

/*  Comando para evitar duplicados de la misma relación cliente-venta */
ALTER TABLE CLIENTES_VENTAS
ADD CONSTRAINT UK_CV_CLIENTE_VENTA UNIQUE (ID_CLIENTE, ID_VENTA);

/* EVIDENCIA: ver tablas creadas */
SELECT TABLE_NAME
FROM USER_TABLES
WHERE TABLE_NAME IN ('VENTAS','CLIENTES_VENTAS')
ORDER BY TABLE_NAME;

