/* Creación tablas CLIENTES */
/* CLIENTES es tabla principal para contactos, direcciones y relaciones */

CREATE TABLE CLIENTES (
  ID_CLIENTE       NUMBER(12)    PRIMARY KEY,
  NOMBRE           VARCHAR2(50)   NOT NULL,
  PRIMER_APELLIDO  VARCHAR2(50)   NOT NULL,
  SEGUNDO_APELLIDO VARCHAR2(50)   NOT NULL
);

CREATE TABLE CORREOS_CLIENTES (
  ID_CORREO_CLIENTE NUMBER(10)   PRIMARY KEY,
  DESCRIPCION       VARCHAR2(100) NOT NULL,
  ID_CLIENTE        NUMBER(12)    NOT NULL,
  CONSTRAINT FK_CORREOS_CLIENTE
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE)
);

CREATE TABLE MEDIOS_PREFERIDOS_CLIENTES (
  ID_MEDIO_PREFERIDO NUMBER(10)   PRIMARY KEY,
  DESCRIPCION        VARCHAR2(100) NOT NULL,
  ID_CLIENTE         NUMBER(12)    NOT NULL,
  CONSTRAINT FK_MEDIO_PREF_CLIENTE
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE)
);

CREATE TABLE TELEFONOS_CLIENTES (
  ID_TELEFONO_CLIENTE NUMBER(10)  PRIMARY KEY,
  NUMERO_TELEFONO     VARCHAR2(20) NOT NULL,
  ID_CLIENTE          NUMBER(12)   NOT NULL,
  CONSTRAINT FK_TELEFONO_CLIENTE
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE)
);

CREATE TABLE DIRECCIONES_CLIENTES (
  ID_DIRECCION  NUMBER(10)    PRIMARY KEY,
  PROVINCIA     VARCHAR2(50)  NOT NULL,
  CANTON        VARCHAR2(50)  NOT NULL,
  DISTRITO      VARCHAR2(50)  NOT NULL,
  SENAS_EXACTAS VARCHAR2(200) NOT NULL,
  ID_CLIENTE    NUMBER(12)    NOT NULL,
  CONSTRAINT FK_DIRECCION_CLIENTE
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE)
);

/* EVIDENCIA: ver que las tablas quedaron creadas en el esquema */
SELECT TABLE_NAME
FROM USER_TABLES
WHERE TABLE_NAME IN (
  'CLIENTES','CORREOS_CLIENTES','MEDIOS_PREFERIDOS_CLIENTES',
  'TELEFONOS_CLIENTES','DIRECCIONES_CLIENTES'
)
ORDER BY TABLE_NAME;

