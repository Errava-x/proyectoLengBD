CREATE OR REPLACE PROCEDURE SP_REGISTRAR_USUARIO (
  P_NOMBRE          IN VARCHAR2,
  P_PRIMER_APELLIDO IN VARCHAR2,
  P_SEGUNDO_APELLIDO IN VARCHAR2,
  P_USERNAME        IN VARCHAR2,
  P_PASS            IN VARCHAR2,
  P_ROL             IN VARCHAR2 DEFAULT 'OPERADOR'
)
IS
  V_ID NUMBER;
BEGIN
  IF P_NOMBRE IS NULL OR TRIM(P_NOMBRE) = '' THEN
    RAISE_APPLICATION_ERROR(-20010, 'El nombre no puede estar vacío');
  ELSIF P_PRIMER_APELLIDO IS NULL OR TRIM(P_PRIMER_APELLIDO) = '' THEN
    RAISE_APPLICATION_ERROR(-20011, 'El primer apellido no puede estar vacío');
  ELSIF P_SEGUNDO_APELLIDO IS NULL OR TRIM(P_SEGUNDO_APELLIDO) = '' THEN
    RAISE_APPLICATION_ERROR(-20012, 'El segundo apellido no puede estar vacío');
  ELSIF P_USERNAME IS NULL OR TRIM(P_USERNAME) = '' THEN
    RAISE_APPLICATION_ERROR(-20013, 'El usuario no puede estar vacío');
  ELSIF P_PASS IS NULL OR TRIM(P_PASS) = '' THEN
    RAISE_APPLICATION_ERROR(-20014, 'La contraseña no puede estar vacía');
  END IF;

  V_ID := SEQ_USUARIO.NEXTVAL;

  INSERT INTO USUARIO (
    ID_USUARIO, NOMBRE, PRIMER_APELLIDO, SEGUNDO_APELLIDO, NOMBRE_USUARIO, CONTRASENA, ROL
  )
  VALUES (
    V_ID, P_NOMBRE, P_PRIMER_APELLIDO, P_SEGUNDO_APELLIDO, P_USERNAME, P_PASS, NVL(P_ROL,'OPERADOR')
  );

  DBMS_OUTPUT.PUT_LINE('Usuario registrado. ID=' || V_ID);

EXCEPTION
  WHEN DUP_VAL_ON_INDEX THEN
    RAISE_APPLICATION_ERROR(-20015, 'Ya existe un usuario con ese nombre');
END;
