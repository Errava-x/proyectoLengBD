CREATE OR REPLACE FUNCTION FN_TOTAL_GASTADO (P_ID_CLIENTE NUMBER)
RETURN NUMBER
IS
  V_TOTAL NUMBER;
BEGIN
  SELECT NVL(SUM(V.MONTO_TOTAL), 0)
  INTO V_TOTAL
  FROM CLIENTES_VENTAS CV
  JOIN VENTAS V ON CV.ID_VENTA = V.ID_VENTA
  WHERE CV.ID_CLIENTE = P_ID_CLIENTE;

  RETURN V_TOTAL;
END;
/

CREATE OR REPLACE FUNCTION FN_EDAD_MASCOTA (P_ID_MASCOTA NUMBER)
RETURN NUMBER
IS
  V_FECHA DATE;
BEGIN
  SELECT FECHA_NACIMIENTO
  INTO V_FECHA
  FROM MASCOTAS
  WHERE ID_MASCOTA = P_ID_MASCOTA;

  RETURN TRUNC(MONTHS_BETWEEN(SYSDATE, V_FECHA) / 12);
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RETURN 0;
END;
/

/*TRIGERS*/

CREATE OR REPLACE TRIGGER TRG_VALIDAR_FECHA_CITA
BEFORE INSERT OR UPDATE ON CITAS
FOR EACH ROW
DECLARE
  V_EXISTE NUMBER;
BEGIN
  SELECT COUNT(*)
  INTO V_EXISTE
  FROM CITAS
  WHERE ID_MASCOTA = :NEW.ID_MASCOTA
    AND TRUNC(FECHA) = TRUNC(:NEW.FECHA)
    AND HORA = :NEW.HORA
    AND ID_CITA != NVL(:NEW.ID_CITA, -1);

  IF V_EXISTE > 0 THEN
    RAISE_APPLICATION_ERROR(-20040, 'Ya existe una cita para esa mascota en esa fecha y hora');
  END IF;
END;
/

