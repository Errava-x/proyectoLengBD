CREATE OR REPLACE PROCEDURE SP_REGISTRAR_USUARIO (
  P_NOMBRE     IN VARCHAR2,
  P_APELLIDO   IN VARCHAR2,
  P_USERNAME   IN VARCHAR2,
  P_PASS       IN VARCHAR2,
  P_ROL        IN VARCHAR2 DEFAULT 'OPERADOR'
)
IS
  V_ID NUMBER;
BEGIN
  IF P_NOMBRE IS NULL OR TRIM(P_NOMBRE) = '' THEN
    RAISE_APPLICATION_ERROR(-20010, 'El nombre no puede estar vacío');
  ELSIF P_APELLIDO IS NULL OR TRIM(P_APELLIDO) = '' THEN
    RAISE_APPLICATION_ERROR(-20011, 'El apellido no puede estar vacío');
  ELSIF P_USERNAME IS NULL OR TRIM(P_USERNAME) = '' THEN
    RAISE_APPLICATION_ERROR(-20012, 'El usuario no puede estar vacío');
  ELSIF P_PASS IS NULL OR TRIM(P_PASS) = '' THEN
    RAISE_APPLICATION_ERROR(-20013, 'La contraseña no puede estar vacía');
  END IF;

  V_ID := SEQ_USUARIO.NEXTVAL;

  INSERT INTO USUARIO (ID_USUARIO, NOMBRE, APELLIDO, NOMBRE_USUARIO, CONTRASENA, ROL)
  VALUES (V_ID, P_NOMBRE, P_APELLIDO, P_USERNAME, P_PASS, NVL(P_ROL,'OPERADOR'));

  DBMS_OUTPUT.PUT_LINE('Usuario registrado. ID=' || V_ID);
EXCEPTION
  WHEN DUP_VAL_ON_INDEX THEN
    RAISE_APPLICATION_ERROR(-20014, 'Ya existe un usuario con ese nombre');
END;
/

CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_USUARIO (
  P_ID        IN NUMBER,
  P_NOMBRE    IN VARCHAR2,
  P_APELLIDO  IN VARCHAR2,
  P_ROL       IN VARCHAR2
)
IS
BEGIN
  UPDATE USUARIO
  SET NOMBRE = P_NOMBRE,
      APELLIDO = P_APELLIDO,
      ROL = P_ROL
  WHERE ID_USUARIO = P_ID;

  IF SQL%ROWCOUNT = 0 THEN
    RAISE_APPLICATION_ERROR(-20022, 'No existe un usuario con ese ID');
  END IF;

  DBMS_OUTPUT.PUT_LINE('Usuario actualizado');
END;
/

CREATE OR REPLACE PROCEDURE SP_ELIMINAR_USUARIO (
  P_ID IN NUMBER
)
IS
BEGIN
  DELETE FROM USUARIO WHERE ID_USUARIO = P_ID;

  IF SQL%ROWCOUNT = 0 THEN
    RAISE_APPLICATION_ERROR(-20023, 'No existe un usuario con ese ID');
  END IF;

  DBMS_OUTPUT.PUT_LINE('Usuario eliminado');
END;
/

CREATE OR REPLACE PROCEDURE SP_LOGIN_USUARIO (
  P_USERNAME   IN  VARCHAR2,
  P_PASS       IN  VARCHAR2,
  P_OK         OUT NUMBER,
  P_ROL        OUT VARCHAR2
)
IS
BEGIN
  SELECT CASE WHEN COUNT(*) = 1 THEN 1 ELSE 0 END,
         MAX(ROL)
  INTO P_OK, P_ROL
  FROM USUARIO
  WHERE NOMBRE_USUARIO = P_USERNAME
    AND CONTRASENA = P_PASS;
END;
/

CREATE OR REPLACE PROCEDURE SP_LISTAR_USUARIOS (
  P_CURSOR OUT SYS_REFCURSOR
)
IS
BEGIN
  OPEN P_CURSOR FOR
    SELECT ID_USUARIO, NOMBRE_USUARIO, ROL
    FROM USUARIO
    ORDER BY ID_USUARIO;
END;
/

CREATE OR REPLACE PROCEDURE SP_BUSCAR_USUARIO (
  P_TEXTO   IN VARCHAR2,
  P_CURSOR  OUT SYS_REFCURSOR
)
IS
BEGIN
  OPEN P_CURSOR FOR
    SELECT ID_USUARIO, NOMBRE_USUARIO, ROL
    FROM USUARIO
    WHERE TO_CHAR(ID_USUARIO) LIKE '%'||P_TEXTO||'%'
       OR UPPER(NOMBRE_USUARIO) LIKE UPPER('%'||P_TEXTO||'%')
       OR UPPER(ROL) LIKE UPPER('%'||P_TEXTO||'%')
    ORDER BY ID_USUARIO;
END;
/

