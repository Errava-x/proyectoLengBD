/*SP, CITAS. Registrar, consultar actualizar, eliminar*/
CREATE OR REPLACE PROCEDURE SP_AGENDAR_CITA (
  P_ID_CITA       IN NUMBER,
  P_FECHA         IN DATE,
  P_HORA          IN VARCHAR2,
  P_TIPO_CONSULTA IN VARCHAR2,
  P_OBSERVACIONES IN VARCHAR2,
  P_ID_MASCOTA    IN NUMBER,
  P_ID_MEDICO     IN NUMBER
)
IS
  V_EXISTE_MASCOTA NUMBER;
  V_EXISTE_MEDICO  NUMBER;
BEGIN
  IF P_FECHA IS NULL THEN
    RAISE_APPLICATION_ERROR(-20052, 'La fecha no puede estar vacía');
  END IF;

  IF TRUNC(P_FECHA) < TRUNC(SYSDATE) THEN
    RAISE_APPLICATION_ERROR(-20053, 'No se puede agendar una cita en fecha pasada');
  END IF;

  SELECT COUNT(*) INTO V_EXISTE_MASCOTA FROM MASCOTAS WHERE ID_MASCOTA = P_ID_MASCOTA;
  IF V_EXISTE_MASCOTA = 0 THEN
    RAISE_APPLICATION_ERROR(-20054, 'No existe una mascota con ese ID');
  END IF;

  SELECT COUNT(*) INTO V_EXISTE_MEDICO FROM MEDICOS WHERE ID_MEDICO = P_ID_MEDICO;
  IF V_EXISTE_MEDICO = 0 THEN
    RAISE_APPLICATION_ERROR(-20055, 'No existe un médico con ese ID');
  END IF;

  INSERT INTO CITAS (ID_CITA, FECHA, HORA, TIPO_CONSULTA, ESTADO, OBSERVACIONES, ID_MASCOTA)
  VALUES (P_ID_CITA, P_FECHA, P_HORA, P_TIPO_CONSULTA, 'Programada', P_OBSERVACIONES, P_ID_MASCOTA);

  INSERT INTO MEDICOS_CITAS (ID_MEDICO_CITA, ID_MEDICO, ID_CITA)
  VALUES (SEQ_MEDICO_CITA.NEXTVAL, P_ID_MEDICO, P_ID_CITA);
END;
/

CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_CITA (
  P_ID_CITA       IN NUMBER,
  P_FECHA         IN DATE,
  P_HORA          IN VARCHAR2,
  P_TIPO_CONSULTA IN VARCHAR2,
  P_ESTADO        IN VARCHAR2,
  P_OBSERVACIONES IN VARCHAR2
)
IS
BEGIN
  UPDATE CITAS
  SET FECHA = P_FECHA,
      HORA = P_HORA,
      TIPO_CONSULTA = P_TIPO_CONSULTA,
      ESTADO = P_ESTADO,
      OBSERVACIONES = P_OBSERVACIONES
  WHERE ID_CITA = P_ID_CITA;

  IF SQL%ROWCOUNT = 0 THEN
    RAISE_APPLICATION_ERROR(-20064, 'No existe una cita con ese ID');
  END IF;
END;
/

CREATE OR REPLACE PROCEDURE SP_ELIMINAR_CITA (
  P_ID_CITA IN NUMBER
)
IS
BEGIN
  DELETE FROM MEDICOS_CITAS WHERE ID_CITA = P_ID_CITA;
  DELETE FROM CITAS WHERE ID_CITA = P_ID_CITA;

  IF SQL%ROWCOUNT = 0 THEN
    RAISE_APPLICATION_ERROR(-20070, 'No existe una cita con ese ID');
  END IF;
END;
/

/* Consultar Citas con filtros (se va a devolver cursor para llenar) */
CREATE OR REPLACE PROCEDURE SP_BUSCAR_CITAS (
  P_ID_CITA       IN NUMBER   DEFAULT NULL,
  P_ID_CLIENTE    IN NUMBER   DEFAULT NULL,
  P_ID_MEDICO     IN NUMBER   DEFAULT NULL,
  P_FECHA         IN DATE     DEFAULT NULL,
  P_TIPO_CONSULTA IN VARCHAR2 DEFAULT NULL,
  P_ID_MASCOTA    IN NUMBER   DEFAULT NULL,
  P_CURSOR        OUT SYS_REFCURSOR
)
IS
  V_SQL   VARCHAR2(4000);
BEGIN
  V_SQL :=
    'SELECT C.ID_CITA, C.FECHA, C.HORA, C.TIPO_CONSULTA, C.ESTADO, C.OBSERVACIONES, ' ||
    'C.ID_MASCOTA, MC.ID_MEDICO, CM.ID_CLIENTE ' ||
    'FROM CITAS C ' ||
    'LEFT JOIN MEDICOS_CITAS MC ON C.ID_CITA = MC.ID_CITA ' ||
    'LEFT JOIN CLIENTES_MASCOTAS CM ON C.ID_MASCOTA = CM.ID_MASCOTA ' ||
    'WHERE 1=1 ';

  IF P_ID_CITA IS NOT NULL THEN
    V_SQL := V_SQL || ' AND C.ID_CITA = ' || P_ID_CITA;
  END IF;

  IF P_ID_CLIENTE IS NOT NULL THEN
    V_SQL := V_SQL || ' AND CM.ID_CLIENTE = ' || P_ID_CLIENTE;
  END IF;

  IF P_ID_MEDICO IS NOT NULL THEN
    V_SQL := V_SQL || ' AND MC.ID_MEDICO = ' || P_ID_MEDICO;
  END IF;

  IF P_FECHA IS NOT NULL THEN
    V_SQL := V_SQL || ' AND TRUNC(C.FECHA) = TO_DATE(''' || TO_CHAR(P_FECHA, 'YYYY-MM-DD') || ''',''YYYY-MM-DD'')';
  END IF;

  IF P_TIPO_CONSULTA IS NOT NULL THEN
    V_SQL := V_SQL || ' AND UPPER(C.TIPO_CONSULTA) LIKE UPPER(''%' || REPLACE(P_TIPO_CONSULTA,'''','''''') || '%'')';
  END IF;

  IF P_ID_MASCOTA IS NOT NULL THEN
    V_SQL := V_SQL || ' AND C.ID_MASCOTA = ' || P_ID_MASCOTA;
  END IF;

  OPEN P_CURSOR FOR V_SQL;
END;
/

